package oop;

public class Kusimus {
    private String kusimus;
    private double x;
    private double y;

    public Kusimus(String kusimus, double x, double y) {
        this.kusimus = kusimus;
        this.x = x;
        this.y = y;
    }

    public String getKusimus() {
        return kusimus;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
